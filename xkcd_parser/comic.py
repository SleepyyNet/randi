class Comic:
    def __init__(self, number = 0, url = "", transcript = "", alt = ""):
        self.number = number
        self.url = number
        self.transcript = transcript
        self.alt = alt

    def setAll (self, number, url, transcript, alt):
        self.number = number
        self.url = number
        self.transcript = transcript
        self.alt = alt
